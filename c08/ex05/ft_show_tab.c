#include <unistd.h>
#include "ft_stock_str.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
		write(1, str++, 1);
}

void	ft_putnbr(int nb)
{
	long int	lnb;
	long int	div;
	long int	mod;

	lnb = nb;
	if (lnb < 0)
	{
		lnb *= -1;
		ft_putchar(45);
	}
	div = lnb / 10;
	mod = lnb % 10;
	if (div != 0)
		ft_putnbr(div);
	ft_putchar(48 + mod);
}

void	ft_show_tab(struct s_stock_str *par)
{
	int	i;

	i = 0;
	while (par[i].str != 0)
	{
		ft_putstr(par[i].str);
		ft_putstr("\n");
		ft_putnbr(par[i].size);
		ft_putstr("\n");
		ft_putstr(par[i].copy);
		ft_putstr("\n");
		i++;
	}
}
